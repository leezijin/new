					<p>SEO作为一种免费的推广方式，我们可以不用花资金就能实现推广的效果。正是由于这种原因，SEO成为了很多个站长的不二选择。但是很多站长朋友只知道SEO，却不了解SEO，更是对一些SEO基础知识一点都不了解，笔者认为，要想做好SEO，就必须在了解SEO基础知识的前提上，所谓知己知彼，方能百战不殆，因此，笔者决定比较系统的谈谈SEO相关的基础知识，希望我们能共进步。</p>

					<h3>一、网站url</h3>
					<p>网站url是很多站长朋友经常忽略的地方，而望着那url的作用却是不可小觑的。一个合理的网站url不仅仅能便于搜索引擎的抓取，更能方便用户的记忆以及判断相应网站的内容。因此，在建站时，必须注意网站url的规范性和合理性。合理的url能便于用户的记忆和搜索引擎对页面的抓取，这是一举两得的事情。</p>
					<p>对于网站哪些不规范的url，可以通过设置301跳转的方式使之跳转到合理的url；为了更好的实现网站url，让搜索引擎爬取我们想要的页面，我们可以使用robots，txtt文件来禁止baiduspide抓取你不想向用户展现的页面。</p>

					<h3>二、title信息</h3>
					<p>网页的title信息是很重要的，它用于告诉用户以及搜索引擎正在访问网页的主要内容是什么，并且当用户在搜索引擎中搜索到相应网页时，相应的title信息就会显示子啊摘要中，除此之外，title信息也是判断一个页面权重的主要参考信息之一。罪域title的书写做到主题明确、简练，并且还要和自身的网站相吻合。</p>

					<h3>三、meta信息</h3>
					<p>对于meta信息，我们主要来讨论metadescription这一部分。Meta description这一部分，Meta description代码的区，用于对网页内容做一个精炼的概括。对于meta description里面的内容，百度会把它当做摘要的选择目标之一，这样更能方便用户从搜索结果中判断你网页的相应内容。</p>

					<h3>四、图片alt</h3>
					<p>alt信息主要是针对图片的，为了seo，笔者建议添加相应的图片alt信息说明。因为这样做可以在网速较慢图片不能显示时让用户明白图片要传达的具体信息，这也能让搜索引擎了解图片的内容。如若使用图片做导航的时候，也应该添加相应的alt信息，这样可以告诉搜索引擎所指向内容的具体信息。</p>

					<h3>五、flash信息</h3>
					<p>搜索引擎只能读懂文本信息，而非文本内容的信息搜索引擎是读不懂的。但是有时候为了界面的美观，必须在网页中添加flash，那么就应该在相应的flash的object标签中添加注释信息，以此来告诉搜索引擎，让搜索引擎明白flash中存放的内容。</p>
					<h3>六、frame框架</h3>
